def perimetro_circulo(radio=3):
    """Calcula el perímetro de un círculo dado su radio.
    Si no se proporciona, el radio por defecto es 3."""
    return 2 * radio

def perimetro_cuadrado(lado):
    """Calcula el perímetro de un cuadrado dado el lado."""
    return 4 * lado

def perimetro_triangulo(lado1, lado2, lado3):
    """Calcula el perímetro de un triángulo dado sus tres lados."""
    return lado1 + lado2 + lado3
